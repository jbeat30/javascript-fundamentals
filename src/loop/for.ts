/**
 * for 문은 반복문 중 하나로, 특정 횟수만큼 반복할 때 사용
 * for 문은 3개의 파트로 나뉘어 있음
 * 1. 변수 선언과 초기화: 반복문을 시작하기 전에 초기 값을 설정
 * 2. 조건 검사: 반복을 계속할지 말지를 결정하는 조건식
 * 3. 변수 증가/감소: 반복문이 한 번 실행될 때마다 변수를 증가시키거나 감소시킴
 *
 * 일반적으로 횟수나 인덱스를 다루는 경우에 사용됨
 *
 * @example
 * for (let i = 0; i < 10; i++) {
 *   console.log(i); // i 값을 출력
 * }
 */

for (let i = 0; i < 10; i++) {
  console.log(i); // 0부터 9까지 출력
}
